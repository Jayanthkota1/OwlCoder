    //Function to check whether the list is palindrome.
    boolean isPalindrome(Node head) 
    {
         Stack<Integer> a=new Stack<Integer>();
         Node n1=head;
         Node n2=head;
         int k=0;
         while(n2!=null && n2.next!=null)
         {
             if(n2.next.next==null)
             {
                 a.push(n1.data);
                 k=1;
                 break;
             }
             a.push(n1.data);
             n1=n1.next;
             n2=n2.next.next;
         }
         n1=n1.next;
         while(n1!=null)
         {
            if(n1.data!=a.pop()) return false;
            n1=n1.next;
         }
         //System.out.print(a);
         return true;
    }    
